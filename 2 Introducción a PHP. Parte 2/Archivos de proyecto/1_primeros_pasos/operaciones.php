<?php

$numero1 = 12;
$numero2  = 4;

//RESTA
$resultado = $numero1 - $numero2;

echo "<h1>RESTA</h1>";
echo "<h2>".$numero1." - ".$numero2." = ".$resultado."</h2>";

//MULTIPLICACIÓN
$resultado2 = $numero1 * $numero2;

echo "<h1>MULTIPLICACION</h1>";
echo "<h2>".$numero1." * ".$numero2." = ".$resultado2."</h2>";

//DIVISIÓN
$resultado2 = $numero1 / $numero2;

echo "<h1>DIVISION</h1>";
echo "<h2>".$numero1." : ".$numero2." = ".$resultado2."</h2>";

//MÓDULO (%)
$numeroA = 11;
$numeroB = 5;

$resultadoModulo = $numeroA % $numeroB;

echo "<h2>".$numeroA." % ".$numeroB." = ".$resultadoModulo."</h2>";

?>
